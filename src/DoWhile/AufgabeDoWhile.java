package DoWhile;

public class AufgabeDoWhile {
    public static void main(String[] args) {

        //Aufgabe A
        int zahlA = 2;
        do {
            System.out.println(zahlA);
            zahlA++;
        } while (zahlA <= 5);

        System.out.println("------------");
        //Aufgabe B

        int zahlB = -10;
        do{
            System.out.print(zahlB +" ");
            zahlB+=3;
        } while(zahlB < 0);

        System.out.println("");
        System.out.println("-------------");

        //Aufgabe C

    }
}
